import random
random.randrange(1, 100)
random.randint(1,5) #随机一个大于等于1且小于等于5的整数
random.randrange(1,10,2)   #随机一个大于等于1且小于等于10之间的奇数，其中2表示递增基数
random.sample([1, 2, 3, 4, 5, 6, 7], 2)#在一个列表里随机取2个数字
print(random.choice(['123','abc',52,[1,2]]))    #随机返回参数列表中任意一个元素
print(random.sample(['123','abc',52,[1,2]],2)) #随机返回参数列表中任意两个元素，参数二指定返回的数量
# 打乱顺序：
lis = [1,2,5,7,9,10]
random.shuffle(lis)
print(lis)

