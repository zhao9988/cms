from datetime import date
from datetime import datetime,timedelta
now = date.today()
print(now)  # 2019-08-07，这是默认的格式
print(now.strftime("%Y.%m.%d"))  # 2019.08.07  格式化时间日期，返回的是时间日期对象对应的字符串
now = datetime.now()  # 获取当前datetime
print(now)
dt = datetime(2015, 4, 19, 12, 20)  # 用指定日期时间创建datetime
print(dt)
dt = datetime(2015, 4, 19, 12, 20)  # 用指定日期时间创建datetime
dt.timestamp()  # 把datetime转换为timestamp
ts = dt.timestamp()  # 把datetime转换为timestamp
print(ts)
cday = datetime.strptime('2015-6-1 18:19:59', '%Y-%m-%d %H:%M:%S')
print(cday)
now = datetime.now()
print(now.strftime('%a, %b %d %H:%M'))
time1 = datetime.now()
print(time1)
print(time1.strftime("%y-%m-%d %H:%M:%S"))
print((time1 + timedelta(days=2, hours=1)).strftime("%y-%m-%d %H:%M:%S"))

